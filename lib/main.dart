Widget panelitem(title, selection) {
  return !selection
      ? Container(
          decoration: BoxDecoration(
              color: Color(0x00ffffff),
              borderRadius: BorderRadius.circular(10)),
          height: 50,
          child: Center(
              child: Text(title,
                  style: TextStyle(
                      decoration: TextDecoration.none,
                      color: HexColor("ffffff"),
                      letterSpacing: 1.5,
                      fontWeight: FontWeight.w300,
                      fontSize: 20,
                      fontFamily: "Teko"))))
      : Container(
          height: 50,
          decoration: BoxDecoration(
            color: Colors.white,
          ),
          child: Center(
              child: Text(title,
                  style: TextStyle(
                      decoration: TextDecoration.none,
                      color: HexColor("000000"),
                      letterSpacing: 1.5,
                      fontWeight: FontWeight.w300,
                      fontSize: 20,
                      fontFamily: "Teko"))));
}

Widget displayContainer(coloring1, coloring2, title, data, icon) {
  return Container(
      decoration: BoxDecoration(
          gradient: LinearGradient(
            begin: Alignment.topCenter,
            end: Alignment.bottomCenter,
            colors: [
              HexColor(coloring1),
              HexColor(coloring2),
            ],
          ),
          borderRadius: BorderRadius.only(
              topLeft: Radius.circular(20), bottomRight: Radius.circular(20))),
      height: 150,
      child: Column(
        children: [
          SizedBox(height: 15),
          Icon(icon, color: Colors.white),
          // SizedBox(height: 20),
          Text(title,
              textAlign: TextAlign.center,
              style: TextStyle(
                  decoration: TextDecoration.none,
                  color: HexColor("ffffff"),
                  letterSpacing: 1.5,
                  fontWeight: FontWeight.w300,
                  fontSize: 25,
                  fontFamily: "Teko")),
          SizedBox(height: 1),
          Text(data,
              textAlign: TextAlign.center,
              style: TextStyle(
                  height: 1,
                  decoration: TextDecoration.none,
                  color: HexColor("ffffff"),
                  letterSpacing: 1.5,
                  fontWeight: FontWeight.w300,
                  fontSize: 50,
                  fontFamily: "Teko"))
        ],
      ));
}

